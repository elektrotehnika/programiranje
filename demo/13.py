def факт(n):
    """Враћа n * n-1 * ... * 1.

    >>> факт(4)
    24
    """
    if n == 0:
        return 1
    else:
        return n * факт(n - 1)

def факт_реп(n):
    """Враћа n * n-1 * ... * 1.

    >>> факт_реп(4)
    24
    """
    return факт_пута(n, 1)

def факт_пута(n, k):
    """Враћа k * n * n-1 * ... * 1.

    >>> факт_пута(4, 3)
    72
    """
    if n == 0:
        return k
    else:
        return факт_пута(n - 1, k * n)

